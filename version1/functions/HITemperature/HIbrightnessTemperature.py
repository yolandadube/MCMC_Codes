def THI(z):

	# arXiv:1709.06099
	# unit is in mK
	term1 = 5.5919*10.0**(-2.0)
	term2 = 2.3242*10.0**(-1.0)*z
	term3 = -2.4136*10.0**(-2.0)*z**2.0

	return term1+term2+term3
